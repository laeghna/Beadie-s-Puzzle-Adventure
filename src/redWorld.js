function updateRedWorld (cols, rows) {
    this.lavaTimer++;
    if (this.lavaTimer % 5 === 0) {
        
        var count = getRandomInt(1, 3);
        var col = 0; 
        var row = 0;
        var i = 0;
        
        while (i < count) {
            
            col = getRandomInt(0, cols);
            row = getRandomInt(0, rows);
            
        if (this.tiles[row * cols + col] === 1)
            this.tiles[row * cols + col] += 1;
        
        i++;
        }
    }
    
    if (this.lavaTimer === this.lavaFrameRate) {
        
        this.lavaTimer = 0;
        for(var c = 0; c < cols; c++) {
            
            for(var r = 0; r < rows; r++) {
                
                var index = r * cols + c;
                var tile = this.tiles[index];
                
                if (tile > 1) {
                    
                    if (tile % this.lavaTileCount === 0) 
                        this.tiles[index] = 1;
                    else
                        this.tiles[index] += 1;
                }
            }
        }
    }
}

function getRedWorldTile (index) {
    var tile = {x: 0, y: 0};
    
    if(this.tiles[index] == 0){
            
            tile.x = this.redTrailX;
            tile.y = this.redTrailY;

    } else {
        switch(this.tiles[index]) {
                
                case 1:
                    tile.x = this.lava1x;
                    break;
                
                case 2:
                    tile.x = this.lava2x;
                    break;
                
                case 3:
                    tile.x = this.lava3x;
                    break;
                
                case 4:
                    tile.x = this.lava4x;
                    break;
                
                case 5:
                    tile.x = this.lava3x;
                    break;
                
                case 6:
                    tile.x = this.lava2x;
                    break;
            }
            
            tile.y = this.lavaY;
    }
    
    return tile;
}

